import { access, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(fileURLToPath(import.meta.url), '..', '..');
const contentDir = path.join(rootDir, 'content');
const outputPath = path.join(
  rootDir,
  'src/features/articles/lib/article-content.generated.ts',
);
const outputDir = path.dirname(outputPath);

async function exists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

function toImportPath(filePath) {
  const relativePath = path
    .relative(outputDir, filePath)
    .split(path.sep)
    .join('/');

  return relativePath.startsWith('.') ? relativePath : `./${relativePath}`;
}

function toSingleQuotedString(value) {
  return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

function toObjectKey(value) {
  return /^[A-Za-z_$][\w$]*$/.test(value)
    ? value
    : toSingleQuotedString(value);
}

async function collectArticles() {
  const slugDirs = await readdir(contentDir, { withFileTypes: true });
  const articles = [];

  for (const slugDir of slugDirs) {
    if (!slugDir.isDirectory()) continue;

    const slug = slugDir.name;
    const slugPath = path.join(contentDir, slug);
    const localeDirs = await readdir(slugPath, { withFileTypes: true });

    for (const localeDir of localeDirs) {
      if (!localeDir.isDirectory()) continue;

      const locale = localeDir.name;
      const filePath = path.join(slugPath, locale, 'index.mdx');

      if (await exists(filePath)) {
        articles.push({ slug, locale, filePath });
      }
    }
  }

  return articles.sort(
    (a, b) => a.slug.localeCompare(b.slug) || a.locale.localeCompare(b.locale),
  );
}

function renderRegistry(articles) {
  const imports = articles.map((article, index) => {
    const importName = `article${index}`;
    return {
      ...article,
      importName,
      importLine: `import ${importName} from '${toImportPath(article.filePath)}?raw';`,
    };
  });

  const bySlug = new Map();
  for (const article of imports) {
    if (!bySlug.has(article.slug)) {
      bySlug.set(article.slug, []);
    }

    bySlug.get(article.slug).push(article);
  }

  const registryEntries = [...bySlug.entries()]
    .map(([slug, translations]) => {
      const localeEntries = translations
        .map(
          (translation) =>
            `    ${toObjectKey(translation.locale)}: ${translation.importName},`,
        )
        .join('\n');

      return `  ${toObjectKey(slug)}: {\n${localeEntries}\n  },`;
    })
    .join('\n');

  return [
    '/* This file is generated by scripts/generate-article-content-registry.mjs. */',
    ...imports.map((article) => article.importLine),
    '',
    "import type { Locale } from '@/types/Locale';",
    '',
    'export const articleContent: Record<string, Partial<Record<Locale, string>>> = {',
    registryEntries,
    '};',
    '',
  ].join('\n');
}

const articles = await collectArticles();
await writeFile(outputPath, renderRegistry(articles));
